private void copyjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_copyjButtonActionPerformed
	if((FileTextField.getText().equals("")) ||(FileSaveTextField.getText().equals(""))){
		JOptionPane.showMessageDialog(this,"没有将文件地址�复制地址��写完�整","信息对话�",JOptionPane.WARNING_MESSAGE);
		return;
	}
	FileHeald fileHeald = new FileHeald();
	java.util.List list = fileHeald.getList(FileTextField.getText());
	if(!list.isEmpty()){
		MyHint hint = new MyHint();
		hint.setVisible(true);
		for(int i = 0 ;i<list.size();i++){
			String strFileName = list.get(i).toString();
			if(strFileName.endsWith(jComboBox1.getSelectedItem().toString())){
				int index = strFileName.lastIndexOf("\\");				
				String strNewName = strFileName.substring(index+1,strFileName.length());
				fileHeald.copyFile(strFileName, FileSaveTextField.getText()+"\\"+strNewName);    				
			}
			
		}
		hint.setVisible(false);
		JOptionPane.showMessageDialog(this, "文件复制完毕", "消息对话框",JOptionPane.WARNING_MESSAGE);
	}
	if(list.isEmpty()){
		JOptionPane.showMessageDialog(this, "文件夹为空","信息对话框",JOptionPane.WARNING_MESSAGE );
	}
}

